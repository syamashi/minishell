#include "../includes/minishell.h"

int		is_space(char c)
{
	return (c == ' ' ||
			c == '\t');
}

int		is_meta(char c)
{
	return (c == '\'' ||
			c == '\"' ||
			c == '&' ||
			c == '<' ||
			c == '>' ||
			c == '|' ||
			c == '\\' ||
//			c == '$' ||
			c == ';');
}

int		is_envmeta(char c)
{
	return (c == '\'' ||
			c == '\"' ||
			c == '&' ||
			c == '<' ||
			c == '>' ||
			c == '|' ||
			c == ';');
}
