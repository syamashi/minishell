#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*str;

	if (!s)
		return (NULL);
	str = (char *)s;
	while (*str)
	{
		if (*str++ == c)
			return (str - 1);
	}
	if (*str == c)
		return (str);
	return (NULL);
}
